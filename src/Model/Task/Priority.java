package Model.Task;

/**
 * This enum class represents the priority of the task.
 * A: what to do now (important and urgent)
 * B: what you need to plan for (important but not urgent)
 * C: what you may be able to delegate (not important but urgent)
 * D: what you can possibly delete (not important and not urgent)
 */
public enum Priority {A, B, C, D}
